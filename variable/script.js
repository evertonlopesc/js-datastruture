console.log("Begin process...");
var num = 1;
num2 = 3;
var price = 1.5;
var myName = "Arroz";
var trueValue = true;
var nullVar = null;
var und;

//Printing using "+" concatenates the variable with the text
console.log("num:" + num);
console.log("num2:" + num2);
console.log("price:" + price);
console.log("myName:" + myName);
console.log("trueValue:" + trueValue);
console.log("nullVar:" + nullVar);
console.log("undefined:" + und);

//Printing using "," add a description e view the content of the variable,
//case it is an object
console.log("num:", num);
console.log("num2:", num2);
console.log("price:", price);
console.log("myName:", myName);
console.log("trueValue:", trueValue);
console.log("nullVar:", nullVar);
console.log("undefined:", und);

//Printing using "${}" concatenates the variable with the text
console.log(`num: ${num}`);
console.log(`num2: ${num2}`);
console.log(`price: ${price}`);
console.log(`myName: ${myName}`);
console.log(`trueValue: ${trueValue}`);
console.log(`nullVar: ${nullVar}`);
console.log(`undefined: ${und}`);

console.log("... End process");
